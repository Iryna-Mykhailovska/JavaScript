// Підрахуй суму всіх чисел в заданому користувачем діапазоні.
let range = prompt("Введіть діапазон чисел (наприклад, 1-10):");
if (range === null || range === '') {
    console.log("Відмовились від введення діапазону.");
}
else {
    let [start, end] = range.split("-").map(Number);
    console.log(start);
    console.log(end);
    if (start < end) {
        let sum = 0;
        for (let i = start; i <= end; i++) {
            sum += i;
        }
        alert(`Сума чисел в діапазоні ${start}-${end}: ${sum}`);
    } else {
        alert("Введено некоректний діапазон.");
    }
}
