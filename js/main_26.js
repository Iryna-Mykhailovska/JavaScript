// Запитай у користувача п’ятирозрядне число і визначи, чи є воно паліндромом.
// Паліндром - це число, яке читається однаково зліва направо і справа наліво (наприклад, 12321).
let fiveDigitNumber = prompt("Введіть п’ятирозрядне число:");   
if (fiveDigitNumber === null || fiveDigitNumber === '') {
    console.log("Відмовились від введення числа.");
}       
else if (fiveDigitNumber.length !== 5 || isNaN(fiveDigitNumber)) {
    alert("Введено некоректне число. Будь ласка, введіть п’ятирозрядне число.");
}   
else {
    let reversedNumber = fiveDigitNumber.split("").reverse().join("");  
    if (fiveDigitNumber === reversedNumber) {
        alert(`Число ${fiveDigitNumber} є паліндромом.`);
    } else {
        alert(`Число ${fiveDigitNumber} не є паліндромом.`);
    }       
}
