// Запитай у користувача 2 числа і знайди найбільший спільний дільник.
let num1 = +prompt("Введіть перше число:");
if (num1 === 0) {
    console.log("Відмовились від введення чисел для пошуку НСД.");
}
else {
    let num2 = +prompt("Введіть друге число:");
    if (num2 === 0) {
        console.log("Відмовились від введення другого числа для пошуку НСД.");
    } else {
        let a = Math.abs(num1);
        let b = Math.abs(num2);
        while (b) {
            let temp = b;
            b = a % b;
            a = temp;
        }
        alert(`Найбільший спільний дільник: ${a}`);
    }
}

