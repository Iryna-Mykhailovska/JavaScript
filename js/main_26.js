// Запитай у користувача число і виведи всі дільники цього числа.*/
let numberForDivisors = +prompt("Введіть число для пошуку його дільників:");
if (numberForDivisors === 0) {
    console.log("Відмовились від введення числа.");
}
else {
    let divisors = [];
    for (let i = 1; i <= numberForDivisors; i++) {
        if (numberForDivisors % i === 0) {
            divisors.push(i);
        }
    }
    alert(`Дільники числа ${numberForDivisors}: ${divisors.join(", ")}`);
}

